export const generateParkingSpots = (): ParkingSpot[] => {
  const spots: ParkingSpot[] = [];
  for (let i = 0; i < 50; i++) {
    spots.push({
      id: `p${i + 1}`,
      zone: i < 40 ? 'main' : 'backup',
      position: {
        x: -9 + (i % 10) * 2,
        y: 0,
        z: 5 + Math.floor(i / 10) * 1.5,
      },
      occupied: Math.random() > 0.12,
      vehiclePlate: Math.random() > 0.12 ? `京A${Math.floor(10000 + Math.random() * 89999)}` : undefined,
    });
  }
  return spots;
};

